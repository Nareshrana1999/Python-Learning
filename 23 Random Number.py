# Random Number

# Python does not have a random()
# function to make a random number, but Python has a built-in module called random that can be used to make random numbers:

# Example
# Import the random module, and display a random number

import random
print(random.random())
print(random.randrange(1,10))
print(random.randrange(1,1000))
print(random.randint(5,100))
print(random.randbytes(10))