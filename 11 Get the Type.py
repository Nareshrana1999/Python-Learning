# Get the Type
# You can get the data type of variable with the type() function.

x = 5        # integer datatype
y = "John"   # sting datatype
a = ["apple", "banana", "cherry"]    # list datatype
b = ("apple", "banana", "cherry")    # tuple datatype
c = {"name" : "John", "age" : 36}    # dict datatype
d = True or False                    # boolean datatype

print(type(x))
print(type(y))
print(type(a))
print(type(b))
print(type(c))
print(type(d))

